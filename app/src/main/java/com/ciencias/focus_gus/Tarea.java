package com.ciencias.focus_gus;


public class Tarea {

    String estado;
    String nombreT;


    public Tarea(String estado, String nombreT) {
        this.estado = estado;
        this.nombreT = nombreT;
    }

    // Getters y Setters

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getNombreT() {
        return nombreT;
    }

    public String toString() {

        return nombreT;

    }

}
